#include "include/math_ops.h"

double calculate_sin(double x) { return sin(x); }

double calculate_cos(double x) { return cos(x); }

double calculate_tan(double x) { return tan(x); }

double calculate_ctg(double x) { return cos(x) / sin(x); }

double calculate_sqrt(double x) { return sqrt(x); }

double calculate_ln(double x) { return log(x); }
